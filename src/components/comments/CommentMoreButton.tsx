import { CommentData } from "@/lib/types";
import {
  DropdownMenu,
  DropdownMenuContent,
  DropdownMenuItem,
  DropdownMenuTrigger,
} from "../ui/dropdown-menu";
import { useState } from "react";
import { Button } from "../ui/button";
import { MoreHorizontalIcon, Trash2Icon } from "lucide-react";
import DeleteCommentDialog from "./DeleteCommentDialog";
import { cn } from "@/lib/utils";
interface CommentMoreButtonProps {
  comment: CommentData;
  className?: string;
}

const CommentMoreButton = ({ comment, className }: CommentMoreButtonProps) => {
  const [showDeleteDialog, setShowDeleteDialog] = useState(false);
  return (
    <>
      <DropdownMenu>
        <DropdownMenuTrigger asChild>
          <Button size="icon" variant="ghost" className={cn(className)}>
            <MoreHorizontalIcon className="size-5 text-muted-foreground" />
          </Button>
        </DropdownMenuTrigger>
        <DropdownMenuContent>
          <DropdownMenuItem
            onClick={() => {
              setShowDeleteDialog(true);
            }}
          >
            <span className="flex cursor-pointer items-center gap-3 text-destructive">
              <Trash2Icon className="size-4" />
              Delete
            </span>
          </DropdownMenuItem>
        </DropdownMenuContent>
      </DropdownMenu>
      <DeleteCommentDialog
        comment={comment}
        open={showDeleteDialog}
        onClose={() => {
          setShowDeleteDialog(false);
        }}
      />
    </>
  );
};

export default CommentMoreButton;
